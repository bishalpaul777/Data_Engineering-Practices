# Logical Operators ----->

# AND operator

x = "Hello" 
y = "India"

print(len(x)<6 and len(y)>4)
print(x[2]=="l" and y[4]=="i")


# OR operator

d = 10
f = 15

print(d/5==2 or f/5==6)
print(pow(d,2)==150 or f*10==170)




# NOT operator

